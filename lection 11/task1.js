/* logArguments
Вам необхідно написати функцію-декоратор logArguments(fn), яка приймає на вхід функцію 
і додає можливість логувати всі аргументи, передані у функцію-аргумент. */

function logArguments(fn) {
  // Повертаємо нову функцію, яка обгортає оригінальну
  return function(...args) {
      // Виводимо в консоль всі передані аргументи
      console.log('Аргументи функції:', args);
      
      // Викликаємо оригінальну функцію з тими ж аргументами
      return fn.apply(this, args);
  }
}