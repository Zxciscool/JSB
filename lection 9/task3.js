/* Вам необхідно написати функцію, яка приймає на вхід масив і повністю
повторює поведінку методу масиву group */

function groupBy(array, key) {
    // Створимо порожній об'єкт для зберігання результатів
    const result = {};
  
    // Перебираємо кожен елемент масиву
    array.forEach(item => {
      // Отримуємо значення ключа для поточної групи
      const groupKey = key(item);
  
      // Якщо група ще не існує, створюємо її як порожній масив
      if (!result[groupKey]) {
        result[groupKey] = [];
      }
  
      // Додаємо поточний елемент до відповідної групи
      result[groupKey].push(item);
    });
  
    return result;
  }